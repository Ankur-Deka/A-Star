# 0: traversible, white
# 1: blocked, gray
# 2: exploring, light cyan
# 3: done exploring, cyan
# 4: start, red
# 5: goal, green
# 6: path, blue
grid_colors = [
    colors.to_rgb("white"),
    colors.to_rgb("gray"),
    tuple([0.8 + v * 0.2 for v in colors.to_rgb("cyan")]),
    colors.to_rgb("cyan"),
    colors.to_rgb("red"),
    colors.to_rgb("lightgreen"),
    colors.to_rgb("blue"),
]
cell_type_codes = dict(
    [
        ("traversible", 0),
        ("blocked", 1),
        ("exploring", 2),
        ("done_exploring", 3),
        ("start", 4),
        ("goal", 5),
        ("path", 6),
    ]
)
grid_cmap = colors.ListedColormap(grid_colors)


def viz_grid(grid_state):
    plt.clf()
    plt.imshow(grid_state, cmap=grid_cmap, vmin=0, vmax=len(grid_colors))

    H, W = grid_state.shape
    rgb = []
    for i in range(H):
        for j in range(W):
            rgb.append(grid_cmap(grid_state[i, j]))
    return (
        (np.array(rgb) * 255)
        .astype(np.uint8)
        .reshape(H, W, -1)
        .repeat(20, axis=0)
        .repeat(20, axis=1)
    )


if __name__ == "__main__":
    grid = get_grid()
    viz_grid(grid)
    plt.show()


def get_dist(a1, a2):
    return np.sum(np.abs(np.array(a1) - np.array(a2)))
